let currentLanguage = 'en';

// Smooth scroll functionality
function scrollToSection(sectionId) {
  const element = document.getElementById(sectionId);
  if (element) {
    element.scrollIntoView({ behavior: 'smooth' });
  }
}

// Toast notification system
function showToast(title, description, type = 'success') {
  const existingToast = document.querySelector('.toast');
  if (existingToast) {
    existingToast.remove();
  }

  const toast = document.createElement('div');
  toast.className = `toast ${type}`;
  toast.innerHTML = `
    <div class="toast-title">${title}</div>
    <div class="toast-description">${description}</div>
  `;

  document.body.appendChild(toast);

  setTimeout(() => {
    toast.style.animation = 'slideIn 0.3s ease-out reverse';
    setTimeout(() => toast.remove(), 300);
  }, 5000);
}

// Form validation and submission
function initContactForm() {
  const form = document.getElementById('contact-form');
  if (!form) return;

  form.addEventListener('submit', function(e) {
    e.preventDefault();

    const formData = {
      name: document.getElementById('name').value.trim(),
      email: document.getElementById('email').value.trim(),
      phone: document.getElementById('phone').value.trim(),
      message: document.getElementById('message').value.trim()
    };

    if (!formData.name || !formData.email || !formData.message) {
      showToast(
        getTranslation('form.errorTitle'),
        getTranslation('form.validationIncomplete'),
        'error'
      );
      return;
    }

    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    if (!emailRegex.test(formData.email)) {
      showToast(
        getTranslation('form.errorTitle'),
        getTranslation('form.validationEmail'),
        'error'
      );
      return;
    }

    const submitBtn = form.querySelector('.btn-submit');
    const originalText = submitBtn.textContent;
    submitBtn.disabled = true;
    submitBtn.textContent = getTranslation('form.sending');

    setTimeout(() => {
      showToast(
        getTranslation('form.successTitle'),
        getTranslation('form.successDescription'),
        'success'
      );
      form.reset();
      submitBtn.disabled = false;
      submitBtn.textContent = originalText;
    }, 1000);
  });
}

function initScrollTriggers() {
  const scrollButtons = document.querySelectorAll('[data-scroll]');
  scrollButtons.forEach(button => {
    button.addEventListener('click', function() {
      const target = this.getAttribute('data-scroll');
      scrollToSection(target);
    });
  });
}

function initLanguageSwitcher() {
  const buttons = document.querySelectorAll('.language-btn');
  buttons.forEach(button => {
    button.addEventListener('click', () => {
      const lang = button.dataset.lang;
      if (lang !== currentLanguage) {
        setLanguage(lang);
      }
    });
  });
}

function applyTranslations(lang) {
  const dictionary = translations[lang];
  if (!dictionary) return;

  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.dataset.i18n;
    const translation = dictionary[key];
    if (!translation) return;
    if (element.dataset.i18nType === 'html') {
      element.innerHTML = translation;
    } else {
      element.textContent = translation;
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
    const key = element.dataset.i18nPlaceholder;
    const translation = dictionary[key];
    if (translation) {
      element.setAttribute('placeholder', translation);
    }
  });

  document.querySelectorAll('[data-i18n-content]').forEach(element => {
    const key = element.dataset.i18nContent;
    const translation = dictionary[key];
    if (translation) {
      element.setAttribute('content', translation);
    }
  });

  document.title = dictionary['meta.title'] || document.title;
}

function setLanguage(lang) {
  if (!translations[lang]) return;
  currentLanguage = lang;
  document.documentElement.lang = lang;
  applyTranslations(lang);
  updateLanguageButtons(lang);
  updateYear();
  saveLanguagePreference(lang);
}

function updateLanguageButtons(lang) {
  document.querySelectorAll('.language-btn').forEach(button => {
    button.classList.toggle('active', button.dataset.lang === lang);
  });
}

function updateYear() {
  const yearElement = document.getElementById('year');
  if (yearElement) {
    yearElement.textContent = new Date().getFullYear();
  }
}

function saveLanguagePreference(lang) {
  try {
    localStorage.setItem('preferredLanguage', lang);
  } catch (error) {
    // Ignore storage errors
  }
}

function getSavedLanguage() {
  try {
    return localStorage.getItem('preferredLanguage');
  } catch (error) {
    return null;
  }
}

function getTranslation(key, lang = currentLanguage) {
  const dictionary = translations[lang] || {};
  return dictionary[key] || translations.en[key] || key;
}

// Initialize all functionality when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
  initContactForm();
  initScrollTriggers();
  initLanguageSwitcher();

  const savedLanguage = getSavedLanguage();
  if (savedLanguage && translations[savedLanguage]) {
    setLanguage(savedLanguage);
  } else {
    setLanguage('en');
  }
});

